package net.line2soft.preambul.views;

import android.graphics.drawable.BitmapDrawable;
import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ImageView;
import net.line2soft.preambul.R;

/**
 * This class represents a detail frament of a picture
 * @author équipe A (Projet Rand'OSM) - Stéphane COATHALEM, Mathieu LUCAS, Adrien PAVIE, Alexis SCOLAN
 */
public class ImageDetailFragment extends Fragment {
    private static final String IMAGE_DATA_EXTRA = "resId";
    private int mImageNum;
    private ImageView mImageView;

    static ImageDetailFragment newInstance(int imageNum) {
        final ImageDetailFragment f = new ImageDetailFragment();
        final Bundle args = new Bundle();
        args.putInt(IMAGE_DATA_EXTRA, imageNum);
        f.setArguments(args);
        return f;
    }

    // Empty constructor, required as per Fragment docs
    public ImageDetailFragment() {}

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        mImageNum = getArguments() != null ? getArguments().getInt(IMAGE_DATA_EXTRA) : -1;
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
            Bundle savedInstanceState) {
        // image_detail_fragment.xml contains just an ImageView
        final View v = inflater.inflate(R.layout.pager_images, container, false);
        mImageView = (ImageView) v.findViewById(R.id.imageViewPager);
        return v;
    }

    @Override
    public void onActivityCreated(Bundle savedInstanceState) {
        super.onActivityCreated(savedInstanceState);
        if (ExcursionInfoActivity.class.isInstance(getActivity())) {
            // Call out to ImageDetailActivity to load the bitmap in a background thread
            ((ExcursionInfoActivity) getActivity()).loadBitmap(mImageNum, mImageView);
        }
    }
    @Override
    public void onStop() {
    	BitmapDrawable img=((BitmapDrawable)mImageView.getDrawable());
    	img.getBitmap().recycle();
    	img=null;
    	super.onDestroy();
    }
}